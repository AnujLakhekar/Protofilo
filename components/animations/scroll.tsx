import { ReactNode, useCallback, useEffect, useRef, useState } from 'react';
import { motion, useInView } from 'motion/react';
import { Github, Globe2Icon } from "lucide-react";
import Link from "next/link";

interface AnimatedItemProps {
    children: ReactNode;
    delay?: number;
    index: number;
    onMouseEnter: (index: number) => void;
    onClick: () => void;
}

const AnimatedItem = ({ children, delay = 0, index, onMouseEnter, onClick }: AnimatedItemProps) => {
    const ref = useRef<HTMLDivElement>(null);
    const inView = useInView(ref, { amount: 0.5 });
    return (
        <motion.div
            ref={ref}
            data-index={index}
            onMouseEnter={() => onMouseEnter(index)}
            onClick={onClick}
            initial={{ scale: 0.7, opacity: 0 }}
            animate={inView ? { scale: 1, opacity: 1 } : { scale: 0.7, opacity: 0 }}
            transition={{ duration: 0.2, delay }}
            className="mb-4 cursor-pointer"
        >
            {children}
        </motion.div>
    );
};

interface AnimatedListItem {
    name: string;
    homepage?: string;
    description?: string;
}

interface AnimatedListProps {
    items?: AnimatedListItem[];
    onItemSelect?: (item: AnimatedListItem, index: number) => void;
    showGradients?: boolean;
    enableArrowNavigation?: boolean;
    className?: string;
    itemClassName?: string;
    displayScrollbar?: boolean;
    initialSelectedIndex?: number;
}

const AnimatedList = ({
    items = [],
    onItemSelect,
    showGradients = true,
    enableArrowNavigation = true,
    className = '',
    itemClassName = '',
    displayScrollbar = true,
    initialSelectedIndex = -1
}: AnimatedListProps) => {
    const listRef = useRef<HTMLDivElement>(null);
    const [selectedIndex, setSelectedIndex] = useState<number>(initialSelectedIndex);
    const [keyboardNav, setKeyboardNav] = useState(false);
    const [topGradientOpacity, setTopGradientOpacity] = useState(0);
    const [bottomGradientOpacity, setBottomGradientOpacity] = useState(1);

    const handleItemMouseEnter = useCallback((index: number) => {
        setSelectedIndex(index);
    }, []);

    const handleItemClick = useCallback(
        (item: AnimatedListItem, index: number) => {
            setSelectedIndex(index);
            if (onItemSelect) {
                onItemSelect(item, index);
            }
        },
        [onItemSelect]
    );

    const handleScroll = useCallback((e: React.UIEvent<HTMLDivElement>) => {
        const { scrollTop, scrollHeight, clientHeight } = e.currentTarget;
        setTopGradientOpacity(Math.min(scrollTop / 50, 1));
        const bottomDistance = scrollHeight - (scrollTop + clientHeight);
        setBottomGradientOpacity(scrollHeight <= clientHeight ? 0 : Math.min(bottomDistance / 50, 1));
    }, []);

    useEffect(() => {
        if (!enableArrowNavigation) return;
        const handleKeyDown = (e: KeyboardEvent) => {
            if (e.key === 'ArrowDown' || (e.key === 'Tab' && !e.shiftKey)) {
                e.preventDefault();
                setKeyboardNav(true);
                setSelectedIndex(prev => Math.min((prev ?? -1) + 1, items.length - 1));
            } else if (e.key === 'ArrowUp' || (e.key === 'Tab' && e.shiftKey)) {
                e.preventDefault();
                setKeyboardNav(true);
                setSelectedIndex(prev => Math.max((prev ?? 0) - 1, 0));
            } else if (e.key === 'Enter') {
                if (selectedIndex >= 0 && selectedIndex < items.length) {
                    e.preventDefault();
                    if (onItemSelect) {
                        onItemSelect(items[selectedIndex], selectedIndex);
                    }
                }
            }
        };

        window.addEventListener('keydown', handleKeyDown);
        return () => window.removeEventListener('keydown', handleKeyDown);
    }, [items, selectedIndex, onItemSelect, enableArrowNavigation]);

    useEffect(() => {
        if (!keyboardNav || selectedIndex < 0 || !listRef.current) return;
        const container = listRef.current as HTMLDivElement;
        const selectedItem = container.querySelector(`[data-index="${selectedIndex}"]`) as HTMLDivElement | null;
        if (selectedItem) {
            const extraMargin = 50;
            const containerScrollTop = container.scrollTop;
            const containerHeight = container.clientHeight;
            const itemTop = selectedItem.offsetTop;
            const itemBottom = itemTop + selectedItem.offsetHeight;
            if (itemTop < containerScrollTop + extraMargin) {
                container.scrollTo({ top: itemTop - extraMargin, behavior: 'smooth' });
            } else if (itemBottom > containerScrollTop + containerHeight - extraMargin) {
                container.scrollTo({
                    top: itemBottom - containerHeight + extraMargin,
                    behavior: 'smooth'
                });
            }
        }
        // eslint-disable-next-line react-hooks/exhaustive-deps
        setKeyboardNav(false);
    }, [selectedIndex, keyboardNav]);

    const [Open, setOpen] = useState(false)

    return (
        <div
            className={`relative w-full max-w-[500px] sm:w-[500px] ${className}`}
        >
            <div
                ref={listRef}
                className={`max-h-[400px] overflow-y-auto p-2 sm:p-4 ${
                    displayScrollbar
                        ? '[&::-webkit-scrollbar]:w-[8px] [&::-webkit-scrollbar-track]:bg-[#060010] [&::-webkit-scrollbar-thumb]:bg-[#222] [&::-webkit-scrollbar-thumb]:rounded-[4px]'
                        : 'scrollbar-hide'
                }`}
                onScroll={handleScroll}
                style={{
                    scrollbarWidth: displayScrollbar ? 'thin' : 'none',
                    scrollbarColor: '#222 #060010'
                }}
            >
                {items.map((item, index) => (
                    <AnimatedItem
                        key={index}
                        delay={0.1}
                        index={index}
                        onMouseEnter={handleItemMouseEnter}
                        onClick={() => handleItemClick(item, index)}
                    >
                        <div
                            className={`p-3 sm:p-4 bg-[#111] rounded-lg ${selectedIndex === index ? 'bg-[#222]' : ''} ${itemClassName}`}>
                            <div className="text-white m-0 flex flex-col sm:flex-row gap-2 sm:gap-3 items-start sm:items-center">
                                <Github/>
                                <span className={"flex flex-col sm:flex-row justify-between w-full gap-1 sm:gap-0"}>
                                    {item?.name}
                                    {item?.homepage &&
                                        <Link onClick={(e) => {
                                            e.preventDefault()
                                            setOpen(true)
                                            if (Open && item?.homepage) {
                                                window.open(item?.homepage, "_blank")
                                            }
                                        }} href={item?.homepage}><Globe2Icon/></Link>}
                                </span>
                            </div>
                        </div>
                    </AnimatedItem>
                ))}
            </div>

            {Open && <div
                className={"fixed top-0 left-0 w-screen h-screen bg-black/50 backdrop-blur-sm z-50 flex items-center justify-center"}
                onClick={() => setOpen(false)}>
                <div className={"bg-[#111] p-6 rounded-lg"}>
                    <h2 className={"text-xl font-bold mb-4"}>{items[selectedIndex]?.name}</h2>
                    <p>{items[selectedIndex]?.description || "No description available."}</p>
                    {items[selectedIndex]?.homepage && (
                        <a href={items[selectedIndex].homepage} target="_blank" rel="noopener noreferrer"
                           className="text-blue-500 mt-4 inline-block">
                            Visit Homepage
                        </a>
                    )}
                </div>
            </div>}

            {showGradients && (
                <>
                    <div
                        className="absolute top-0 left-0 right-0 h-[50px] bg-gradient-to-b from-black to-transparent pointer-events-none transition-opacity duration-300 ease"
                        style={{ opacity: topGradientOpacity }}
                    ></div>
                    <div
                        className="absolute bottom-0 left-0 right-0 h-[100px] bg-gradient-to-t from-black to-transparent pointer-events-none transition-opacity duration-300 ease"
                        style={{ opacity: bottomGradientOpacity }}
                    ></div>
                </>
            )}
        </div>
    );
};

export default AnimatedList;
